void print_mapping_character(uint8_t c)
{
    int map_index;

    if(c > ' ')
    {
        map_index = char_map[c];
    }
    else
    {
        switch(c)
        {
            case 1:
                // curPalette = getNextChar();
                puts("SetCurPalette nextChar");
                return;
            case 2:
                // map_index = getNextChar();
                puts("SetMapIndex nextChar");
                // break;
                // now just for parsing, so return directly
                return;
            case 10:
                // y += module[0].height;
                puts("Line Feed (LF)");
                return;
            case ' ':
                // x += module[0].width;
                puts("Space");
                return;
            default:
                puts("none");
                return;
        }
    }

    // (/ui.f chunk 1) fm_num[0] = 0x59
    if(map_index >= 0x59)
    {
        map_index -= 0x59;
        // Frame frame_index
        printf("Frame %03d\n", map_index);
    }
    else
    {
        // FModule fm_index (module_index, x, y, transform)
        printf("FModule %02d (%02d, %02d, %02d, %02d)\n", map_index, fm_data[map_index * 4], fm_data[map_index * 4 + 1], fm_data[map_index * 4 + 2], fm_data[map_index * 4 + 3]);
    }
    return;
}

int main()
{
    for(int i = 0; i < 256; i++)
    {
        printf("%03d:   ", i);
        print_mapping_character(i);
    }
    return 0;
}
